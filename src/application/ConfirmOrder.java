    private void btnProceedOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProceedOrderActionPerformed
        // confirm order
        String order_status = "draft";
        String address = adrs.getText();

        if(address.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter your recidential address");
        }
        else{
            try {
                JOptionPane.showMessageDialog(null, "Thank you for choosing FAST YUMMY\nYour Order will recieve soon");

                String sql1 = "update ORDERTABLE set ADDRESS = '"+address+"' where ADDRESS = '"+order_status+"'";//insert address to the current order
                prestmnt = conn.prepareStatement(sql1);
                prestmnt.execute();//execute sql1 command
                adrs.setText("");
                this.setVisible(false);
            } 
            catch (SQLException ex) {
                Logger.getLogger(ConfirmOrder.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            
    }//GEN-LAST:event_btnProceedOrderActionPerformed

