    private void btnChangeOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeOrderActionPerformed
        String order = "draft";
        int dialogButton = JOptionPane.YES_NO_OPTION;//create yes, no dialog buttons
        int answer = JOptionPane.showConfirmDialog(null,"Do you want to change the order?", "Warning",dialogButton);//show confirm-dialog button
        if(answer == JOptionPane.YES_OPTION){
            try {
                String sql1 = "DELETE FROM ORDERTABLE WHERE ADDRESS = '"+order+"'";//delete draft order
                prestmnt = conn.prepareStatement(sql1);
                prestmnt.execute();
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(ConfirmOrder.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnChangeOrderActionPerformed

