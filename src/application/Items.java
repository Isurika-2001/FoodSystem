    public void addTable(int noOfItem, double price, String name){
        int val = noOfItem;
        double tot = val * price;//current order total
        totAmount = tot;//assign current order total to overall order total
                
        //calculate total amount
        for(int i = 0; i<noOfRow; i++){//add every order in the table, to the overall order amount
            Amount = (double)itemTable.getValueAt(i, 3);
            totAmount = totAmount + Amount;
        }
        if(val>0 && noOfRow<16){
            itemTable.setValueAt(name,noOfRow,0);
            itemTable.setValueAt(price,noOfRow,1);
            itemTable.setValueAt(val,noOfRow,2);
            itemTable.setValueAt(tot,noOfRow,3);
            itemTable.setValueAt("TOTAL AMOUNT",16,0);
            itemTable.setValueAt(totAmount,16,3);
            noOfRow++;
        }
        else if(noOfRow == 16){
            JOptionPane.showMessageDialog(null, "You cannot add more items");
        }
        else if(val==0){
            JOptionPane.showMessageDialog(null, "Please select quantity");
        }
        else{
            JOptionPane.showMessageDialog(null, "You can't add negetive number of products");
        }
    }
    
