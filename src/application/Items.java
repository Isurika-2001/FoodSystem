    private void cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelActionPerformed
        //clear order
         DefaultTableModel model = (DefaultTableModel) itemTable.getModel();
        int getSelectedRowForDeletion = itemTable.getSelectedRow();
        if (getSelectedRowForDeletion >= 0 && getSelectedRowForDeletion!=16) {
            try{
                double temp = (double) model.getValueAt(getSelectedRowForDeletion, 3);
                double temp1 = (double) itemTable.getValueAt(16,3);

                model.removeRow(getSelectedRowForDeletion);
                preRows--;//reduse the number of rows in the table by 1

                totAmount = temp1 - temp;
                //set previous total as null
                itemTable.setValueAt(null,preRows,0);
                itemTable.setValueAt(null,preRows,3);
                JOptionPane.showMessageDialog(null, "Row Deleted");
                noOfRow--;//reduse number of rows that user added to the order by 1

                model.addRow(new Object[]{"TOTAL",null, null, totAmount});
                preRows++;
            }
            catch(NullPointerException e){
                JOptionPane.showMessageDialog(null, "Selected record is empty");
            }
        }
        else if(getSelectedRowForDeletion==16){
            JOptionPane.showMessageDialog(null, "You cannot edit Total");
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a record");
        }
    }//GEN-LAST:event_cancelActionPerformed

    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        // check whether customer loged in to the system or not
        
        String foodName = "";
        for(int i = 0; i<noOfRow; i++){
            foodName = foodName + " " + "["+(String)itemTable.getValueAt(i, 0)+ " :" + (int)itemTable.getValueAt(i, 2)+ "]";
        }
        
        if(totAmount>0){
            int index = 1;
            do{
                id = "ORD" + index;
                try {
                    //check for current place order record
                    String sql = "SELECT * FROM ORDERTABLE WHERE ID = '"+id+"'";
                    prestmnt = conn.prepareStatement(sql);
                    Reltset = prestmnt.executeQuery();

                    if (Reltset.next()){
                        index ++;
                    }
                    else{
                        index = -1;
                    }
                } 
                catch (SQLException ex) {
                     Logger.getLogger(FoodOrderingSystem.class.getName()).log(Level.SEVERE, null, ex);
                }
            }while(index != -1);
            
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");  
            LocalDateTime now = LocalDateTime.now();
            
            
            String sql = "Insert into ORDERTABLE values (?,?,?,?,?)";//create sql commamd to insert data into the Oder table
            
            
            
            try{
                //insert draft order data into the data base
                prestmnt = conn.prepareStatement(sql);
                prestmnt.setString(1, id);
                prestmnt.setDouble(2, totAmount);
                prestmnt.setString(3, "draft");
                prestmnt.setString(4, foodName);
                prestmnt.setString(5, (dtf.format(now)));
                prestmnt.execute();//execute the command
            } 
            catch (SQLException ex) {
                Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            String loginStatus = lblLogImage.getText();//get login status
            
            if("".equals(loginStatus)){
                Login login = new Login();
                login.setVisible(true);
            }
            else{
                ConfirmOrder ov = new ConfirmOrder();
                ov.setVisible(true);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select items to place your order");
        }
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

